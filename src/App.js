class App extends Component {
  state = {
    articles: []
  }
  componentDidMount() {
    fetch('http://www.mocky.io/v2/5dc01bc8310000b288be3e37')
      .then(res => res.json())
      .then((data) => {
        console.log(data)
        this.setState({ articles: data.articles })
      })
      .catch(console.log)
  }
  render() {
    const { articles } = this.state;
    return (
      <SearchAppBar articleArr={articles}/>
    );
  }
}

export default App;